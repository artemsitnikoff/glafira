"""Единый нормализатор телефонов в формат хранения: ЦИФРЫ БЕЗ '+' (79991234567).

Формат хранения в БД (по требованию заказчика) = `79991234567` — цифры с кодом
страны, БЕЗ ведущего '+'. Это ровно то, что отдаёт `_normalize_contact` (дедуп),
поэтому хранение, дедуп, поиск и Манго-матчинг используют один и тот же вид.
'+' подставляется там, где он нужен (Telegram-резолв) — не в БД.

Используется всеми писателями phone-полей (candidates.phone, users.phone).
"""

from .candidate_dedup import _clean_phone


def normalize_phone(value: str | None) -> str | None:
    """Нормализовать телефон в формат хранения (цифры без '+': 79991234567).

    Контракт:
    - None или пустая строка → None
    - строка без цифр (например, «—», «нет», «n/a») → None (не телефон)
    - иначе → цифры с кодом страны без '+' (8→7, 10-значный → 7+номер)

    Не кидает исключений.

    Примеры:
        '89991234567'        → '79991234567'
        '79991234567'        → '79991234567'  (идемпотентно)
        '9991234567'         → '79991234567'
        '+7 999 123-45-67'   → '79991234567'
        '+7(999)123-45-67'   → '79991234567'
        '+79991234567'       → '79991234567'
        '+380991234567'      → '380991234567'
        None / '' / '—'      → None
    """
    if not value:
        return None

    cleaned = _clean_phone(str(value))

    # _clean_phone при отсутствии цифр возвращает сырьё (без '+').
    # Признак «не телефон»: нет '+' (он всегда добавляется при наличии цифр).
    if not cleaned or not cleaned.startswith('+'):
        return None

    result = cleaned[1:]  # убираем '+' → формат хранения
    return result or None  # '+' без цифр → None (не пустая строка)
