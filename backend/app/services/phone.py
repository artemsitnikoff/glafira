"""Единый нормализатор телефонов в формат E.164 (+79991234567).

Используется всеми писателями phone-полей (candidates.phone, users.phone).
Дедуп и Манго-матчинг оперируют только цифрами через _normalize_contact —
они формат-агностичны и не меняются при хранении E.164.
"""

from .candidate_dedup import _clean_phone


def normalize_phone_e164(value: str | None) -> str | None:
    """Нормализовать телефон в E.164 (+79991234567).

    Контракт:
    - None или пустая строка → None
    - строка без цифр (например, «—», «нет», «n/a») → None (не телефон)
    - иначе → результат _clean_phone (E.164)

    Не кидает исключений.

    Примеры:
        '89991234567'        → '+79991234567'
        '79991234567'        → '+79991234567'
        '9991234567'         → '+79991234567'
        '+7 999 123-45-67'   → '+79991234567'
        '+7(999)123-45-67'   → '+79991234567'
        '+79991234567'       → '+79991234567'  (идемпотентно)
        None                 → None
        ''                   → None
        '—'                  → None
        'нет'                → None
    """
    if not value:
        return None

    cleaned = _clean_phone(str(value))

    # _clean_phone при отсутствии цифр возвращает сырьё (до 20 символов).
    # Признак «не телефон»: в результате нет '+' (который всегда добавляется при наличии цифр).
    if not cleaned or not cleaned.startswith('+'):
        return None

    return cleaned
