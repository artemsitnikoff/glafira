def build_employee_summary_prompt(facts: dict) -> tuple[str, str]:
    """Returns (system, user) для AI-сводки сотрудника."""

    system = """Ты — HR-аналитик. Твоя задача — создать краткую сводку о сотруднике на основе фактических данных.

КРИТИЧЕСКИ ВАЖНО:
• Опирайся ИСКЛЮЧИТЕЛЬНО на переданные факты. Любая цифра, имя, событие, которого нет во входящих данных — строго запрещено
• Если surveys_count менее 3 — НЕ делай обобщений типа "стабильно"/"в целом"/"как правило", описывай конкретно
• Если trend равен None — НИКОГДА не пиши про динамику настроения или удовлетворённости, её нельзя оценить по одному опросу
• Если active_alerts пуст — НЕ упоминай "всё хорошо" или подобные утверждения без фактического обоснования
• 3-5 предложений максимум, без воды, без markdown форматирования
• Если данных мало — пиши коротко и честно, указывая на недостаток информации

Помни: это данные о реальном человеке. Недопустимо выдумывать или додумывать информацию."""

    # Формируем читаемое представление фактов
    adapt_status = f"День адаптации {facts['adapt_day']} из {facts['probation_days']}"

    if facts['surveys_count'] == 0:
        surveys_info = "Опросов не проводилось"
    elif facts['surveys_count'] == 1:
        surveys_info = f"Опросов отвечено: 1 (недостаточно для оценки тренда)"
        if facts['last_survey'] and facts['last_survey'].get('overall_score'):
            surveys_info += f", оценка: {facts['last_survey']['overall_score']}"
    else:
        surveys_info = f"Опросов отвечено: {facts['surveys_count']}"
        if facts['avg_score']:
            surveys_info += f", средняя оценка: {facts['avg_score']:.1f}"
        if facts['trend'] and facts['trend'] != 'None':
            trend_map = {'rising': 'растёт', 'falling': 'падает', 'stable': 'стабильна'}
            surveys_info += f", динамика: {trend_map.get(facts['trend'], facts['trend'])}"
        else:
            surveys_info += " (тренд недоступен)"

    plan_info = f"План адаптации: выполнено {facts['plan_progress']['done']} из {facts['plan_progress']['total']}"
    if facts['overdue_items']:
        plan_info += f", просрочено: {len(facts['overdue_items'])} пунктов"

    alerts_info = f"Активных алертов: {len(facts['active_alerts'])}"
    if facts['active_alerts']:
        levels = [alert['level'] for alert in facts['active_alerts']]
        alerts_info += f" ({', '.join(levels)})"

    user_prompt = f"""ДАННЫЕ О СОТРУДНИКЕ:

Имя: {facts['full_name']}
Должность: {facts['position'] or 'Не указана'}
Статус адаптации: {adapt_status}
Уровень риска: {facts['risk_level']}

{surveys_info}

{plan_info}

{alerts_info}

Просьба: напиши краткую профессиональную сводку (3-5 предложений) о текущем состоянии адаптации этого сотрудника."""

    return system, user_prompt


